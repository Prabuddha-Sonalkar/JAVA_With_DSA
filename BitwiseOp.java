public class BitwiseOp {
    public static void main(String[] args) {
        // Bitwise operators

        // 1.Bitwiswe AND
        System.out.println(5 & 6);

        // 2. OR
        System.out.println(5 | 6);

        // 3. XOR
        System.out.println(5 ^ 6);

        // 4. one's compliment
        System.out.println(~5);

        // 5. left shift
        System.out.println(5 << 2);

        // 5. right shift
        System.out.println(6 >> 1);
    }
}
